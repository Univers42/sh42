#include "ft_wctype.h"

int	ft_iswxdigit(wint_t wc)
{
	return ((wc >= L'0' && wc <= L'9')
		|| (wc >= L'A' && wc <= L'F')
		|| (wc >= L'a' && wc <= L'f'));
}
